package gustavo.mywine.app.model;

public class RSS {

    private int id;
    private String url;
    private String name;
    private int userId;
    public static final String TABLE_NAME = "tbl_rss";

    public void RSS(){

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }
}
